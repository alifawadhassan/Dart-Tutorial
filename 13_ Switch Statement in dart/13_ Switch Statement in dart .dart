void main() 
{
  String colour= "black";
  print("Switch Statement: \n");
  
  switch(colour)
  {
    case "red":
      print("color is red");
      break;
      
    case "green":
      print("color is green");
      break;
      
    case "black":
      print("color is black");
      break;
      
    default:
      print("Invalid Color");
  }
}  
